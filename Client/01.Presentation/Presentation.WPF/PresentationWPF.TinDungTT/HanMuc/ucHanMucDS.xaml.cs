        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void nudPageSize_ValueChanged(object sender, RadRangeBaseValueChangedEventArgs e)
        {
            if (raddgrHanMucDS != null && raddgrHanMucDS.DataContext != null)
            {
                DataTable dt = ((DataView)raddgrHanMucDS.DataContext).Table;
                if (dt != null)
                {
                    radPage.PageSize = (int)nudPageSize.Value;
                    raddgrHanMucDS.DataContext = dt.DefaultView;
                }
            }
        }

